const int N=1e5+8;

bool sign[N];

int n,ma,u[N],cnt[N];

LL ans;

vector<int> d[N];

stack<LL> s;

void pre(){
    for(int i=1;i<=ma;++i){
        for(int j=i;j<=ma;j+=i) d[j].push_back(i);
        if (i==1) u[i]=1;
        else if (i/d[i][1]%d[i][1]==0) u[i]=0;
        else u[i]=-u[i/d[i][1]];
    }
}

int coprime(int x){
    int tmp=0;
    for(int i:d[x])
        tmp+=u[i]*cnt[i];
    return tmp;
}

void updata(int x,int val){
    for(int i:d[x])
        cnt[i]+=val;
}

